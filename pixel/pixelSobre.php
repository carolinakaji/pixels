            <hr>
            <p>Você já viu os jogos de video-game dos 80 e 90? Para quem era criança nessa época, deve
                se lembrar dos jogos de Atari, Mega Drive, Super Nintendo. Pois os graficos para esses
                video-games eram feitos em PIXEL.</p>
            <p>
                Mas você sabe o que que é um PIXEL? O Pixel é o menor ponto de uma imagem digital.
                O conjunto de pixels formam a imagem como um todo.
            </p>
            <p>
                Já o Pixel Art é uma forma de arte digital que consiste na criação de elementos gráficos pela composição de um conjunto de pixels.</p>

            <h2 class="py-3">História</h2>
            <p> O termo “pixel art” ficou conhecido a partir de 1982, numa publicação de Adele Goldberg e Robert Flegal do Centro de Pesquisas da Xerox em Palo Alto.</p>
            <p>O pixel está limitado ao sistema de cores que a máquina utiliza, como o RGB, que utiliza uma combinação de cores vermelho, verde e azul conseguindo exibir uma paleta de mais de 10mil cores. A quantidade de cores, varia de acordo com a evolução do hardware.</p>
            <p>Os hardwares antigos possuiam uma limitação de cores que implicava em qualidade de
                imagens menos realistas.</p>
            <p>O uso do pixel art, antigamente, era limitado a games e sistemas computacionais, pois os dispositivos somente permitiam este tipo de desenho. Com o aprimoramento das resoluções e técnicas de geração de gráficos, o pixel art foi perdendo espaço. Entretanto, ainda hoje, existe uma comunidade bastante ativa e profissionais produzindo jogos retro.</p>
            <p>Na época, as animações em pixel art, eram realizadas por linhas de códigos que ao serem compiladas, viravam pixels, sendo sua criação algo complexo.
            </p>
            <p>A partir da década de 90, computadores com interfaces gráficas tornaram-se mais populares, sendo ete um grande avaço para a criação da arte. Com programas como o Paint da Microsoft, a criação de pixel arts se tornaram mais simples de ser compreendidas e produzidas.</p>

            <h2 class="py-3">Atualmente</h2>
            <p>Mesmo sendo uma técnica antiga, utilizada em jogos 2D o pixel art ainda coleciona
                amantes e profissionais que não deixam esta arte tão incrível ser esquecida com as novas gerações de jogos e artes.</p>
            <p>Jogos em estilo retro, continuam a ser desenvolvidos, como o caso do artista brasileiro
                Glauber Kotaki. Ele participou na produção de vários jogos, como Rogue Legacy e Rogue Legacy 2, 30XX, Deep Dungeons of Doom entre outros.</p>
            <p>Um jogo que também faz a cabeça de crianças e adultos é o Minecraft, cujo design é
                totalmente diferente e inesperado quando comparado com gráficos de consoles atuais.
                Com a montagem de cenários e personagens por blocos, remete ao pixel art, porém
                com 3 dimensões.</p>

